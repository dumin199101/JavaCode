package com.goosuu.code03;

/**
 *  public static long round():四舍五入
 */
public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(-34));
        System.out.println(Math.ceil(4.6));
        System.out.println(Math.floor(3.6));
        System.out.println(Math.round(3.1));
        System.out.println(Math.random());
        System.out.println(Math.max(1,5));
        System.out.println(Math.min(34,62));
    }
}
